//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define PLUGIN_BRUSH(RelativePath,...) FSlateImageBrush(FSQLStyle::InContent(RelativePath,".png"),__VA_ARGS__)

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

TSharedPtr<FSlateStyleSet> FSQLStyle::StyleSet = nullptr;
TSharedPtr<ISlateStyle> FSQLStyle::Get() {return StyleSet;}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

FString FSQLStyle::InContent(const FString &RelativePath, const ANSICHAR* Extension) {
	static FString Content = IPluginManager::Get().FindPlugin("USQLite")->GetContentDir();
	return (Content/RelativePath)+Extension;
}

FName FSQLStyle::GetStyleSetName() {
	static FName StyleName(TEXT("USQLStyle"));
	return StyleName;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void FSQLStyle::Initialize() {
	if (StyleSet.IsValid()) {return;}
	//
	const FVector2D Icon16x16(16.0f,16.0f);
	const FVector2D Icon128x128(128.0f,128.0f);
	//
	StyleSet = MakeShareable(new FSlateStyleSet(GetStyleSetName()));
	StyleSet->SetContentRoot(IPluginManager::Get().FindPlugin("USQLite")->GetContentDir());
	//
	StyleSet->Set("ClassIcon.SQLite", new PLUGIN_BRUSH(TEXT("Icons/USQLite_16x"),Icon16x16));
	StyleSet->Set("ClassThumbnail.SQLite", new PLUGIN_BRUSH(TEXT("Icons/USQLite_128x"),Icon128x128));
	//
	FSlateStyleRegistry::RegisterSlateStyle(*StyleSet.Get());
};

void FSQLStyle::Shutdown() {
	if (StyleSet.IsValid()) {
		FSlateStyleRegistry::UnRegisterSlateStyle(*StyleSet.Get());
		ensure(StyleSet.IsUnique());
		StyleSet.Reset();
	}///
}

